# Pida al usuario el espacio recorrido por un coche y el tiempo que ha tardado en horas y que calcule a qué velocidad media había realizado el recorrido.
E= int(input("Introduce el espacio recorrido en km "))
T= int(input("Introduce el tiempo que ha tardado en horas "))
V= E/T
print (f"La velocidad media ha sido de {V}km/h")